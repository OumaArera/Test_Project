def compare_entries(first_person, second_person, user_input):
    '''
    Compares the first_person follower_count
    With the second_person follower count
    Compares the greater of the two tho user_input and returns True or False
    '''

    #store the follower_count of first_person to a variable
    value_first_person = first_person["follower_count"]

    # store the follower_count of second_person to a variable
    value_second_person = second_person["follower_count"]

    person_with_maximum_value = []

    #We need to compare the two follower_ counts
    #We store the maximum value for comparison with user_input
    if value_first_person > value_second_person:

        person_with_maximum_value = first_person['name']

    elif value_second_person > value_first_person:

        person_with_maximum_value = second_person['name']

    #We compare the value of the person with the maximum follower_count
    #To the user_input and determine if user is right
    if person_with_maximum_value == user_input:
        return True

    else:
        return False



def play_high_low():
    '''
    Creates a loop that enables the game to keep playing
    '''

    #We create a while loop that ensures the player keeps playing
    playing = True
    while playing:

        #We create a variable that will store the user score and
        #tell the player how many he got right
        score = 0

        #We need to create another while loop
        #to continue current game
        guessing = True
        while guessing:

            #Assign two accounts name to display
            account1 = assign()
            account2 = assign()

            #In case the player got the first question right
            #and now the value of score is greater than 0
            #In that case we assign account1 to account2
            #and we assign account2 to new personality
            if score > 0:
                account1 = account2
                account2 = assign()

                #We need to ensure that account2 is not
                #the one that was assign account1 previously
                #because in that case they would be the same and our
                #code would hit a dead end and stop
                if account1 == account2:
                    account2 = assign()

            #Display account1 name and description
            print(f"\nName: {account1['name']}. \n\nDescription: {account1['description']}")

            #Display the "Versus"
            print("\nVersus")

            #Display account2 name and description
            print(f"\nName: {account2['name']}. \n\nDescription: {account2['description']}")

            #Print current score
            print(f"\nCurrent score = {score}")

            #Ask user to guess
            guess = input(f"\nEnter the name of the person with higher followers btween {account1['name']} and {account2['name']}: ")

            #See if player got it right
            if compare_entries(account1,account2,guess):
                #If player is correct,
                #add 1 to score
                #Continue with iteration
                continue
            else:
                guessing = False

        #The player did not get it right in the first place
        #Ask them if they want to continue playing
        play_again = input("\nWant to play again? Y or N: ").lower()
        print(f"\nCurrent score = {score}")

        #If they want to play again, continue
        #Else if they do not want to play, exit the game
        #Else throw an error and exit the game

        if play_again == "y":
            continue

        elif play_again == "n":
            playing = False
            print("\nGame exit!")

        else:
            print("\nInvalid input!")
            play_again = input("\nWant to play again? Y or N: ").lower()



